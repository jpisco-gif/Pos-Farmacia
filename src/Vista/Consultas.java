    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
      
         String campo = txtid.getText();
         String where = "";
        if (!"".equals(campo)) {
            where = "WHERE idProducto = '" + campo + "'";
        }
        try {
            DefaultTableModel modelo = new DefaultTableModel();
            tabla.setModel(modelo);

            PreparedStatement ps = null;
            ResultSet rs = null;
            ConexionB conn = new ConexionB();
            Connection con = conn.getConnection();

            String sql = "SELECT idProducto, idCategoria, idProveedor, codBarras, nombre, precioCompra, precioVenta, FechaCaducidad, stock, estado FROM producto " + where;
            System.out.println(sql);
            ps = con.prepareStatement(sql);
            rs = ps.executeQuery();

            ResultSetMetaData rsMd = (ResultSetMetaData) rs.getMetaData();
            int cantidadColumnas = rsMd.getColumnCount();
            modelo.addColumn("Codigo");
            modelo.addColumn("Categoria");
            modelo.addColumn("Proveedor");
            modelo.addColumn("Cod. Barras");
            modelo.addColumn("Nombre");
            modelo.addColumn("Precio compra");     
            modelo.addColumn("Precio venta c/u");
            modelo.addColumn("Fecha vencimiento");
            modelo.addColumn("Stock");
            modelo.addColumn("Estado");  
            while (rs.next()) {
                Object[] filas = new Object[cantidadColumnas];
                for (int i = 0; i < cantidadColumnas; i++) {
                    filas[i] = rs.getObject(i + 1);
                }
                modelo.addRow(filas);
            }

        } catch (Exception ex) {
            System.err.println(ex.toString());
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

